package com.bstek.dorado.ofc.binding;


public class PieBindingConfig extends ChartBindingConfig {
	private String valueProperty;
	private String labelProperty;
	private String labelColorProperty;
	private String textProperty;
	private String fontSizeProperty;
	private String toolTipProperty;
	
	public String getValueProperty() {
		return valueProperty;
	}
	public void setValueProperty(String valueProperty) {
		this.valueProperty = valueProperty;
	}
	public String getLabelProperty() {
		return labelProperty;
	}
	public void setLabelProperty(String labelProperty) {
		this.labelProperty = labelProperty;
	}
	public String getLabelColorProperty() {
		return labelColorProperty;
	}
	public void setLabelColorProperty(String labelColorProperty) {
		this.labelColorProperty = labelColorProperty;
	}
	public String getTextProperty() {
		return textProperty;
	}
	public void setTextProperty(String textProperty) {
		this.textProperty = textProperty;
	}
	public String getFontSizeProperty() {
		return fontSizeProperty;
	}
	public void setFontSizeProperty(String fontSizeProperty) {
		this.fontSizeProperty = fontSizeProperty;
	}
	public String getToolTipProperty() {
		return toolTipProperty;
	}
	public void setToolTipProperty(String toolTipProperty) {
		this.toolTipProperty = toolTipProperty;
	}
	
	
}
