package com.bstek.dorado.ofc.binding;

public class StackBindingConfig extends ChartBindingConfig{
	private String valuesProperty;
	private String valueProperty;
	private String colorProperty;
	/**
	 * @return the valuesProperty
	 */
	public String getValuesProperty() {
		return valuesProperty;
	}
	/**
	 * @param valuesProperty the valuesProperty to set
	 */
	public void setValuesProperty(String valuesProperty) {
		this.valuesProperty = valuesProperty;
	}
	/**
	 * @return the valueProperty
	 */
	public String getValueProperty() {
		return valueProperty;
	}
	/**
	 * @param valueProperty the valueProperty to set
	 */
	public void setValueProperty(String valueProperty) {
		this.valueProperty = valueProperty;
	}
	/**
	 * @return the colorProperty
	 */
	public String getColorProperty() {
		return colorProperty;
	}
	/**
	 * @param colorProperty the colorProperty to set
	 */
	public void setColorProperty(String colorProperty) {
		this.colorProperty = colorProperty;
	}
	
}
