package com.bstek.dorado.ofc.model;

public class LineDot {
	private String type;
	private String color;
	private Integer haloSize;
	private Integer size;
	private String toolTip;
	private Double value;
	private Double x;
	private Double y;
	
	private Integer sides;
	private Integer rotation;
	
	private boolean hollow;
	private String backgroundColor;
	private String backgroundAlpha;
	private Double width;
	/**
	 * @return the type
	 */
	public String getType() {
		return type;
	}
	/**
	 * @param type the type to set
	 */
	public void setType(String type) {
		this.type = type;
	}
	/**
	 * @return the color
	 */
	public String getColor() {
		return color;
	}
	/**
	 * @param color the color to set
	 */
	public void setColor(String color) {
		this.color = color;
	}
	/**
	 * @return the haloSize
	 */
	public Integer getHaloSize() {
		return haloSize;
	}
	/**
	 * @param haloSize the haloSize to set
	 */
	public void setHaloSize(Integer haloSize) {
		this.haloSize = haloSize;
	}
	/**
	 * @return the size
	 */
	public Integer getSize() {
		return size;
	}
	/**
	 * @param size the size to set
	 */
	public void setSize(Integer size) {
		this.size = size;
	}
	/**
	 * @return the toolTip
	 */
	public String getToolTip() {
		return toolTip;
	}
	/**
	 * @param toolTip the toolTip to set
	 */
	public void setToolTip(String toolTip) {
		this.toolTip = toolTip;
	}
	/**
	 * @return the value
	 */
	public Double getValue() {
		return value;
	}
	/**
	 * @param value the value to set
	 */
	public void setValue(Double value) {
		this.value = value;
	}
	/**
	 * @return the x
	 */
	public Double getX() {
		return x;
	}
	/**
	 * @param x the x to set
	 */
	public void setX(Double x) {
		this.x = x;
	}
	/**
	 * @return the y
	 */
	public Double getY() {
		return y;
	}
	/**
	 * @param y the y to set
	 */
	public void setY(Double y) {
		this.y = y;
	}
	/**
	 * @return the sides
	 */
	public Integer getSides() {
		return sides;
	}
	/**
	 * @param sides the sides to set
	 */
	public void setSides(Integer sides) {
		this.sides = sides;
	}
	/**
	 * @return the rotation
	 */
	public Integer getRotation() {
		return rotation;
	}
	/**
	 * @param rotation the rotation to set
	 */
	public void setRotation(Integer rotation) {
		this.rotation = rotation;
	}
	/**
	 * @return the hollow
	 */
	public boolean isHollow() {
		return hollow;
	}
	/**
	 * @param hollow the hollow to set
	 */
	public void setHollow(boolean hollow) {
		this.hollow = hollow;
	}
	/**
	 * @return the backgroundColor
	 */
	public String getBackgroundColor() {
		return backgroundColor;
	}
	/**
	 * @param backgroundColor the backgroundColor to set
	 */
	public void setBackgroundColor(String backgroundColor) {
		this.backgroundColor = backgroundColor;
	}
	/**
	 * @return the backgroundAlpha
	 */
	public String getBackgroundAlpha() {
		return backgroundAlpha;
	}
	/**
	 * @param backgroundAlpha the backgroundAlpha to set
	 */
	public void setBackgroundAlpha(String backgroundAlpha) {
		this.backgroundAlpha = backgroundAlpha;
	}
	/**
	 * @return the width
	 */
	public Double getWidth() {
		return width;
	}
	/**
	 * @param width the width to set
	 */
	public void setWidth(Double width) {
		this.width = width;
	}
	
	
	
	
}
